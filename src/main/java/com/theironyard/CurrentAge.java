public class CurrentAge {
    int years;
    int months;
    int days;
    int hours;
    int minutes;
    int seconds;

    public CurrentAge() {
    }

    public CurrentAge(int years, int months, int days, int hours, int minutes, int seconds) {

        this.years = years;
        this.months = months;
        this.days = days;
        this.hours = hours;
        this.minutes = minutes;
        this.seconds = seconds;
    }
}
